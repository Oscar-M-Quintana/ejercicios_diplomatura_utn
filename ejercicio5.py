""" Tome dos valores por consola y guarde en una lista.
Muestre en pantalla la lista. """

primer_valor = int(input("Ingrese un número:"))
segundo_valor = int(input("Ingrese un segundo número:"))
la_suma_de_los_valores = primer_valor + segundo_valor
lista = [primer_valor, segundo_valor, la_suma_de_los_valores]

print(lista)
